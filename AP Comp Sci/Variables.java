
public class Variables {
    
    public static void main(String[] args){
        
		// Basic Variables
		int x = 5;
		int y = -10;
		double dub = 45.233;
		boolean allergicToBees = true;
		String shoutOut = "Hey ya";
		
		System.out.println("x = " + x);
		System.out.println("y = " + y);
		System.out.println("dub = " + dub);
		System.out.println("allergicToBees = " + allergicToBees);
		System.out.println("shoutOut = " + shoutOut);
		
    }
}